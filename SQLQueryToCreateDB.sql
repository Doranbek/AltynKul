CREATE TABLE Departments
(
	Id int PRIMARY KEY IDENTITY NOT NULL,
	Title text NOT NULL,
	Specification text NULL	
);

CREATE TABLE Positions
(
	Id int PRIMARY KEY IDENTITY NOT NULL,
	Title text NOT NULL,
	Specification text NULL
);

CREATE TABLE Camps
(
	Id int PRIMARY KEY IDENTITY NOT NULL,
	Title nvarchar(30) NOT NULL,
	Specification text NULL
);
CREATE TABLE Categories
(
	Id int PRIMARY KEY IDENTITY NOT NULL,
	Title nvarchar(40) NOT NULL,
	Specification text NULL
);
CREATE TABLE Rooms
(
	Id int PRIMARY KEY IDENTITY NOT NULL,
	RoomNumber int NOT NULL,
	CategoryId int NOT NULL,
	Amount int NOT NULL,
	Specification text NULL,
	FOREIGN KEY (CategoryId)  REFERENCES Categories(Id)
);
CREATE TABLE Applications
(
	Id int PRIMARY KEY IDENTITY NOT NULL,
	FullName text NOT NULL,
	DepartmentId int NOT NULL,
	PositionId int NOT NULL,
	CreatedDate Date CONSTRAINT DF_CD DEFAULT GETDATE(),	
	CampId int NOT NULL,
	CampersNumber int NOT NULL,	
	CategoryId int,
	Status bit NOT NULL,
	FOREIGN KEY (DepartmentId)  REFERENCES Departments(Id),
	FOREIGN KEY (PositionId)  REFERENCES Positions(Id),
	FOREIGN KEY (CampId)  REFERENCES Camps(Id),
	FOREIGN KEY (CategoryId)  REFERENCES Categories(Id)
);
CREATE TABLE Vouchers
(
	Id int PRIMARY KEY IDENTITY NOT NULL,
	FullName nvarchar(60) NOT NULL,
	CampId int NOT NULL,
	RoomId int NOT NULL,
	CategoryId int NOT NULL,
	Cost decimal(18,0) NOT NULL,
	Reserved bit NOT NULL,
	PayStatus bit NOT NULL,
	ApplicationId int NOT NULL,	
	FOREIGN KEY (CategoryId)  REFERENCES Categoryes (Id) ON DELETE SET NULL,
	FOREIGN KEY (CampId)  REFERENCES Camps(Id),
	FOREIGN KEY (RoomId)  REFERENCES Rooms(Id),
	FOREIGN KEY (ApplicationId)  REFERENCES Applications(Id)
);
CREATE TABLE CampCategories
(
	Id int PRIMARY KEY IDENTITY NOT NULL,
	CampId int NOT NULL,
	CategoryId int NOT NULL,
	Price int NOT NULL,	
	FOREIGN KEY (CampId)  REFERENCES Camps(Id),
	FOREIGN KEY (CategoryId)  REFERENCES Categories(Id)
);

